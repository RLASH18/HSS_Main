        document.addEventListener('DOMContentLoaded', function() {
            const searchInput = document.getElementById('searchInput');
            const mobileSearchInput = document.getElementById('mobileSearchInput');
            const categoriesSection = document.getElementById('categories-section');

            // Function to handle search
            function handleSearch(searchTerm) {
                if (window.itemList) {
                    if (searchTerm === '') {
                        window.itemList.search();
                        if (categoriesSection) {
                            categoriesSection.style.display = 'block';
                        }
                    } else {
                        window.itemList.search(searchTerm);
                        if (categoriesSection) {
                            categoriesSection.style.display = 'none';
                        }
                    }
                }
            }

            // Sync desktop and mobile search inputs
            if (searchInput && mobileSearchInput) {
                searchInput.addEventListener('input', function() {
                    mobileSearchInput.value = this.value;
                    handleSearch(this.value.trim());
                });

                mobileSearchInput.addEventListener('input', function() {
                    searchInput.value = this.value;
                    handleSearch(this.value.trim());
                });

                // Handle Escape key for both inputs
                [searchInput, mobileSearchInput].forEach(input => {
                    input.addEventListener('keydown', function(e) {
                        if (e.key === 'Escape') {
                            searchInput.value = '';
                            mobileSearchInput.value = '';
                            handleSearch('');
                            this.blur();
                        }
                    });
                });
            } else if (searchInput) {
                // Fallback for desktop only
                searchInput.addEventListener('input', function() {
                    handleSearch(this.value.trim());
                });

                searchInput.addEventListener('keydown', function(e) {
                    if (e.key === 'Escape') {
                        this.value = '';
                        handleSearch('');
                        this.blur();
                    }
                });
            }

            // Mobile menu toggle functionality
            const mobileMenuToggle = document.getElementById('mobile-menu-toggle');
            const mobileMenuDropdown = document.getElementById('mobile-menu-dropdown');

            if (mobileMenuToggle && mobileMenuDropdown) {
                mobileMenuToggle.addEventListener('click', function(e) {
                    e.stopPropagation();
                    mobileMenuDropdown.classList.toggle('hidden');
                });

                // Close menu when clicking outside
                document.addEventListener('click', function(e) {
                    if (!mobileMenuToggle.contains(e.target) && !mobileMenuDropdown.contains(e.target)) {
                        mobileMenuDropdown.classList.add('hidden');
                    }
                });

                // Close menu when clicking a link
                const menuLinks = mobileMenuDropdown.querySelectorAll('a');
                menuLinks.forEach(link => {
                    link.addEventListener('click', function() {
                        mobileMenuDropdown.classList.add('hidden');
                    });
                });
            }

            // Header icon active state functionality
            const currentPath = window.location.pathname;
            const headerIcons = document.querySelectorAll('[data-route]');

            headerIcons.forEach(icon => {
                const route = icon.getAttribute('data-route');
                if (route && currentPath.startsWith(route)) {
                    // Check if it's a header-icon (desktop) or mobile menu item
                    if (icon.classList.contains('header-icon')) {
                        // Desktop icon styling
                        icon.classList.add('active');
                        
                        const svg = icon.querySelector('svg');
                        const span = icon.querySelector('span');
                        
                        if (svg) {
                            svg.classList.remove('text-gray-700');
                            svg.classList.add('text-white');
                        }
                        
                        if (span) {
                            span.classList.remove('text-gray-600');
                            span.classList.add('text-white');
                        }
                    } else {
                        // Mobile dropdown menu styling
                        icon.classList.remove('hover:bg-gray-50');
                        icon.classList.add('bg-[#815331]');

                        const svg = icon.querySelector('svg');
                        const span = icon.querySelector('span');

                        if (svg) {
                            svg.classList.remove('text-gray-700');
                            svg.classList.add('text-white');
                        }

                        if (span) {
                            span.classList.remove('text-gray-700');
                            span.classList.add('text-white');
                        }
                    }
                }
            });
        });
